package session45;
public class bitwiseDemo {
	public static void main(String[] args) {
		System.out.println(5&2);
		System.out.println(5|2);
		System.out.println(5^5);
		System.out.println(5^2);
		System.out.println(5>>1);
		System.out.println(5>>2);
		System.out.println(5<<1);
		System.out.println(5<<2);
		System.out.println(~5);
	}
}
